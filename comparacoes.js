// == (comparação implícita)

const numero = 5;
const texto = "5";

console.log(texto == numero);
// saida: true

// === (comparação explícita)

const numero1 = 5;
const texto1 = "5";

console.log(texto1 === numero1);
// saida: false

// typeof
console.log(typeof numero);
console.log(typeof texto);

// == só comparar o valor
// === compara o valor e o tipo de dado
